function getRandom(min, max) {
  return Math.floor(Math.random() * (max - min) + min)
}
 
const  initialState = {
  people: persons,
  searchPeople: ''
}
 
export const suggestReducer = (state = initialState, action) => {
    switch (action.type) {
      case "INPUT_CHANGE":
        return {...state, searchPeople: action.text}
    default:
      return state
    } 
}

export const inputChanged = (e) => ({
    type: "INPUT_CHANGE",
    text: e.target.value
})



